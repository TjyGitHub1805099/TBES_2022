/*******************************************************************************
 * Function : 程序运行 指示灯 1s闪烁
 ******************************************************************************/
//==program run led indicate
void app_LED_RUN(UINT16 cycle)
{
	static UINT32 ledRunTick=0;
	if(ledRunTick++%cycle == 0)
	{
		hal_gpio_set_do_toggle( (enumDoLineType)(LED_RUN) );
	}
}
